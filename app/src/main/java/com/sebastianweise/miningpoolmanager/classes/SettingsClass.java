package com.sebastianweise.miningpoolmanager.classes;

public class SettingsClass {
    String email;
    Integer monitor;
    Integer minPayout;


    public String getEmail() { return email; }

    public void setEmail(String email) { this.email = email; }

    public Integer getMonitor() { return monitor; }

    public void setMonitor(Integer monitor) { this.monitor = monitor; }

    public Integer getMinPayout() { return minPayout; }

    public void setMinPayout(Integer minPayout) { this.minPayout = minPayout; }
}
